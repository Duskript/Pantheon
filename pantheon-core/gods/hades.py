def run_hades(compile_limit: int = 5) -> HadesReport:
    """Run the complete Hades consolidation pipeline.

    Returns a HadesReport with all findings.
    """
    report = HadesReport()
    start = time.time()

    # Load environment variables from .env files for in-process operations
    # (compilation, distillation) — extraction subprocess loads its own
    _home = os.path.expanduser("~")
    for _env_path in [Path(f"{_home}/.hermes/.env"), Path(f"{_home}/pantheon/.env")]:
        if _env_path.exists():
            for line in _env_path.read_text().split("\n"):
                line = line.strip()
                if line and "=" in line and not line.startswith("#"):
                    k, v = line.split("=", 1)
                    v = v.strip().strip("'\"")
                    os.environ.setdefault(k.strip(), v)

    try:
        # Phase 1: Health checks
        logger.info("Hades: Running health checks...")
        try:
            health = run_health_checks()
            report.health = health
            logger.info("  → %d codexes checked", len(health.get("codexes", {})))
        except Exception as exc:
            report.errors.append(f"Health checks failed: {exc}")
            logger.exception("Hades health check error")

        # Phase 2: Distillation
        logger.info("Hades: Running distillation...")
        try:
            dist = run_distillation()
            report.distillation = dist
            logger.info("  → %d sessions → %d distilled files",
                         dist.get("sessions_processed", 0),
                         dist.get("distilled_files_written", 0))
        except Exception as exc:
            report.errors.append(f"Distillation failed: {exc}")
            logger.exception("Hades distillation error")

        # Phase 3: Archive scan
        logger.info("Hades: Scanning for archive candidates...")
        try:
            arch = run_archive()
            report.archive = arch
            logger.info("  → %d archive candidates found", len(arch.get("candidates", [])))
        except Exception as exc:
            report.errors.append(f"Archive scan failed: {exc}")
            logger.exception("Hades archive error")

        # Phase 4: Shared Context Sweep — ingest >24h entries from ~/pantheon/shared/
        logger.info("Hades: Running shared context sweep...")
        try:
            sc = run_shared_context_sweep()
            report.shared_context = sc
            logger.info("  → %d decisions ingested, %d active tasks archived",
                         sc.get("decisions_ingested", 0),
                         sc.get("active_archived", 0))
            if sc.get("errors"):
                logger.warning("  → %d shared context errors", len(sc["errors"]))
        except Exception as exc:
            report.errors.append(f"Shared context sweep failed: {exc}")
            logger.exception("Hades shared context sweep error")

        # Phase 5: Entity extraction from Athenaeum files
        logger.info("Hades: Running entity extraction...")
        try:
            _REAL_HOME = os.path.expanduser("~")
            extract_script = os.path.join(_REAL_HOME, "athenaeum", "scripts", "extract-entities.py")
            if os.path.exists(extract_script):
                import subprocess, sys
                env = os.environ.copy()
                for _env_path in [Path(f"{_REAL_HOME}/.hermes/.env"), Path(f"{_REAL_HOME}/.hermes/profiles/hephaestus/.env"), Path(f"{_REAL_HOME}/pantheon/.env")]:
                    if _env_path.exists():
                        for line in _env_path.read_text().split("\n"):
                            line = line.strip()
                            if line and "=" in line and not line.startswith("#"):
                                k, v = line.split("=", 1)
                                env.setdefault(k.strip(), v.strip().strip("'\""))
                result = subprocess.run(
                    [sys.executable, extract_script, "--workers", "4"],
                    capture_output=True, text=True, timeout=3600, env=env,
                )
                if result.returncode == 0:
                    # Parse summary from output
                    for line in result.stdout.split("\n"):
                        if "Total entities extracted:" in line:
                            try:
                                report.extraction["entities_extracted"] = int(line.split(":")[-1].strip())
                            except (ValueError, IndexError):
                                pass
                        elif "Total relations extracted:" in line:
                            try:
                                report.extraction["relations_extracted"] = int(line.split(":")[-1].strip())
                            except (ValueError, IndexError):
                                pass
                        elif "Processed:" in line and "files" in line.lower():
                            try:
                                report.extraction["files_processed"] = int(line.split(":")[-1].strip().split()[0])
                            except (ValueError, IndexError):
                                pass
                        elif "Failed:" in line and "files" in line.lower():
                            try:
                                report.extraction["files_failed"] = int(line.split(":")[-1].strip().split()[0])
                            except (ValueError, IndexError):
                                pass
                    logger.info("  → %d entities, %d relations extracted",
                                 report.extraction["entities_extracted"],
                                 report.extraction["relations_extracted"])
                else:
                    err = result.stderr.strip()[:2000] if result.stderr.strip() else "exit code != 0"
                    report.extraction["error"] = f"extract-entities failed: {err}"
                    logger.warning("  → Entity extraction failed: %s", err)
            else:
                report.extraction["error"] = f"Script not found: {extract_script}"
                logger.warning("  → Entity extraction script not found")
        except subprocess.TimeoutExpired:
            report.extraction["error"] = "extract-entities timed out after 20m"
            logger.warning("  → Entity extraction timed out")
        except Exception as exc:
            report.extraction["error"] = f"Entity extraction error: {exc}"
            logger.exception("Hades entity extraction error")

        # Phase 6: LLM Compilation — compile un-compiled sessions into knowledge articles
        if compile_limit > 0:
            logger.info("Hades: Running LLM compilation (limit=%d)...", compile_limit)
            try:
                comp = run_compilation(limit=compile_limit)
                report.compilation = comp
                logger.info("  → %d sessions compiled, %d articles created, %d errors",
                             comp.get("sessions_compiled", 0),
                             comp.get("articles_created", 0),
                             len(comp.get("errors", [])))
            except Exception as exc:
                report.errors.append(f"LLM compilation failed: {exc}")
                logger.exception("Hades compilation error")
        else:
            logger.info("Hades: LLM compilation skipped (--no-compile)")

        # Phase 7: Scan Codex-General for topic clusters → auto-create new Codices
        logger.info("Hades: Scanning Codex-General for topic clusters...")
        try:
            new_codexes = _scan_general_for_codexes()
            if new_codexes:
                report.health["new_codexes"] = new_codexes
                logger.info("  → %d new Codexes created from General scan", len(new_codexes))
        except Exception as exc:
            report.errors.append(f"Codex-General scan failed: {exc}")

        # Phase 8: Deliver report to Hermes mailbox
        logger.info("Hades: Delivering report to Hermes mailbox...")
        try:
            msg_path = deliver_to_mailbox(report)
            if msg_path:
                logger.info("  → Delivered: %s", msg_path)
        except Exception as exc:
            report.errors.append(f"Mailbox delivery failed: {exc}")
            logger.exception("Hades mailbox delivery error")

        elapsed = time.time() - start
        logger.info("Hades complete in %.2fs", elapsed)

        # Phase 9: Write heartbeat for the Fates
        import sys
        _hades_scripts = os.path.join(os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__)))), "scripts")
        if _hades_scripts not in sys.path:
            sys.path.insert(0, _hades_scripts)
        try:
            from heartbeat import beat  # noqa: F811
            beat("hades")
            logger.info("  → Heartbeat written")
        except Exception as exc:
            logger.warning("  → Heartbeat write failed (non-fatal): %s", exc)

    except Exception as exc:
        report.errors.append(f"Hades run failed: {exc}")
        logger.exception("Hades run failed")

    return report


