class HadesReport:
    """Report generated by a Hades run."""

    def __init__(self):
        self.timestamp: str = datetime.now(timezone.utc).isoformat()
        self.health: Dict[str, Any] = {
            "codexes": {},
            "chroma_vs_fs": {},
            "orphans": {"chroma_only": [], "fs_unembedded": []},
            "missing_indexes": [],
            "stale_candidates": [],
        }
        self.distillation: Dict[str, Any] = {
            "sessions_processed": 0,
            "distilled_files_written": 0,
            "by_codex": {},
        }
        self.archive: Dict[str, Any] = {
            "files_archived": 0,
            "errors": [],
        }
        self.compilation: Dict[str, Any] = {
            "sessions_compiled": 0,
            "articles_created": 0,
            "articles_updated": 0,
            "articles_by_type": {"concept": 0, "connection": 0, "qa": 0},
            "transcripts_copied": 0,
            "errors": [],
            "batch_size": 0,
            "total_backlog": 0,
        }
        self.extraction: Dict[str, Any] = {
            "files_processed": 0,
            "files_failed": 0,
            "entities_extracted": 0,
            "relations_extracted": 0,
            "error": None,
        }
        self.shared_context: Dict[str, Any] = {
            "decisions_ingested": 0,
            "active_archived": 0,
            "athenaeum_entries": 0,
            "errors": [],
        }
        self.errors: List[str] = []

    def to_dict(self) -> Dict:
        return {
            "timestamp": self.timestamp,
            "health": self.health,
            "distillation": self.distillation,
            "archive": self.archive,
            "compilation": self.compilation,
            "extraction": self.extraction,
            "shared_context": self.shared_context,
            "errors": self.errors,
        }

    def to_markdown(self) -> str:
        """Format the report as a human-readable markdown summary."""
        lines = [
            f"# 🌙 Hades Nightly Report — {self.timestamp[:10]}",
            "",
            "## 📊 Codex Overview",
            "| Codex | Files | Embedded | Distilled | Sessions | Missing INDEX |",
            "|---|---|---|---|---|---|",
        ]

        for codex, info in sorted(self.health.get("codexes", {}).items()):
            missing = "⚠️" if codex in self.health.get("missing_indexes", []) else "✅"
            lines.append(
                f"| {codex} | {info.get('files', 0)} | {info.get('embedded', '?')} | "
                f"{info.get('distilled', 0)} | {info.get('sessions', 0)} | {missing} |"
            )

        lines.append("")

        # Chroma vs FS consistency
        orphans_fs = self.health.get("orphans", {}).get("fs_unembedded", [])
        orphans_c = self.health.get("orphans", {}).get("chroma_only", [])
        if orphans_fs or orphans_c:
            lines.append("## ⚠️ Consistency Issues")
            if orphans_fs:
                lines.append(f"- **{len(orphans_fs)} files not embedded in ChromaDB**")
            if orphans_c:
                lines.append(f"- **{len(orphans_c)} ChromaDB entries orphaned** (no matching file)")

        # Missing INDEXes
        missing = self.health.get("missing_indexes", [])
        if missing:
            lines.append(f"\n### Missing INDEX.md ({len(missing)})")
            for m in missing:
                lines.append(f"- {m}")

        # INDEX auto-creation report
        created = self.health.get("indexes_created", [])
        if created:
            lines.append(f"\n### ✅ INDEX.md Auto-Created ({len(created)})")
            for c in created:
                lines.append(f"- `{c}/INDEX.md` created")

        # Stale candidates
        stale = self.health.get("stale_candidates", [])
        if stale:
            lines.append(f"\n### 🗄️ Stale Candidates ({len(stale)})")
            lines.append("Files not modified in 90+ days:")
            for s in stale[:10]:
                lines.append(f"- {s['path']} (last modified: {s['last_modified'][:10]})")
            if len(stale) > 10:
                lines.append(f"- ... and {len(stale) - 10} more")

        # Distillation summary
        dist = self.distillation
        if dist.get("distilled_files_written", 0) > 0:
            lines.append(f"\n## 🔄 Distillation")
            lines.append(f"- Sessions processed: {dist.get('sessions_processed', 0)}")
            lines.append(f"- Distilled files written: {dist.get('distilled_files_written', 0)}")

        # Compilation summary
        comp = self.compilation
        if comp.get("sessions_compiled", 0) > 0 or comp.get("errors"):
            lines.append("")
            lines.append(f"\n## 🧠 LLM Compilation")
            lines.append(f"- Sessions compiled: {comp.get('sessions_compiled', 0)}")
            lines.append(f"- Articles created: {comp.get('articles_created', 0)}")
            lines.append(f"- Articles updated: {comp.get('articles_updated', 0)}")
            by_type = comp.get("articles_by_type", {})
            if any(by_type.values()):
                lines.append(f"  - Concepts: {by_type.get('concept', 0)}")
                lines.append(f"  - Connections: {by_type.get('connection', 0)}")
                lines.append(f"  - Q&A: {by_type.get('qa', 0)}")
            lines.append(f"- Transcripts copied: {comp.get('transcripts_copied', 0)}")
            backlog = comp.get("total_backlog", 0)
            if backlog > 0:
                lines.append(f"- Remaining backlog: {backlog}")
            if comp.get("errors"):
                for e in comp["errors"]:
                    lines.append(f"- ❌ {e}")
            new_cx_list = comp.get("new_codexes_created", [])
            if new_cx_list:
                lines.append(f"- ✨ New Codices: {', '.join(new_cx_list)}")

        # Archive summary
        arch = self.archive
        if arch.get("files_archived", 0) > 0 or len(arch.get("candidates", [])) > 0:
            lines.append("")
            lines.append(f"## 📦 Archive")
            lines.append(f"- Files archived: {arch.get('files_archived', 0)}")
            if arch.get("candidates"):
                lines.append(f"- Candidates found: {len(arch['candidates'])}")

        # Entity extraction summary
        extr = self.extraction
        if extr.get("entities_extracted", 0) > 0 or extr.get("error"):
            lines.append("")
            lines.append(f"## 🔗 Entity Extraction")
            if extr.get("error"):
                lines.append(f"- ❌ Error: {extr['error']}")
            else:
                lines.append(f"- Files processed: {extr.get('files_processed', 0)}")
                lines.append(f"- Entities extracted: {extr.get('entities_extracted', 0)}")
                lines.append(f"- Relations extracted: {extr.get('relations_extracted', 0)}")
                if extr.get("files_failed", 0) > 0:
                    lines.append(f"- ⚠️ Files failed: {extr['files_failed']}")

        # Shared context sweep summary
        sc = self.shared_context
        if sc.get("decisions_ingested", 0) > 0 or sc.get("active_archived", 0) > 0 or sc.get("athenaeum_entries", 0) > 0:
            lines.append("")
            lines.append(f"## 📋 Shared Context Sweep")
            lines.append(f"- Decisions ingested: {sc.get('decisions_ingested', 0)}")
            lines.append(f"- Active tasks archived: {sc.get('active_archived', 0)}")
            lines.append(f"- Athenaeum write entries noted: {sc.get('athenaeum_entries', 0)}")
            if sc.get("errors"):
                for e in sc["errors"]:
                    lines.append(f"- ⚠️ {e}")
        elif sc.get("errors"):
            lines.append("")
            lines.append(f"## 📋 Shared Context Sweep")
            for e in sc["errors"]:
                lines.append(f"- ⚠️ {e}")

        # New Codices created
        new_codexes = self.health.get("new_codexes", [])
        if new_codexes:
            lines.append("")
            lines.append(f"## ✨ New Codices Created ({len(new_codexes)})")
            for nc in new_codexes:
                lines.append(f"- **{nc.get('codex')}** — {nc.get('files_moved', 0)} files from '{nc.get('keyword', '?')}' cluster")

        # Errors
        if self.errors:
            lines.append(f"\n## ❌ Errors ({len(self.errors)})")
            for e in self.errors:
                lines.append(f"- {e}")

        lines.append("")
        lines.append("---")
        lines.append(f"_Report generated: {self.timestamp}_")

        return "\n".join(lines)


